public class Hero {
    private int life;
    private int level;

    public Hero() {
        this.life = 50;
        this.level = 5;
    }

    public int getLife() {
        return life;
    }

    public void setLife(int life) {
        this.life = life;
    }

    public int getLevel() {
        return level;
    }

    public void setLevel(int level) {
        this.level = level;
    }

}
